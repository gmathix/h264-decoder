void precompute_level_scale_table(CodecContext *ctx, const uint8_t *weight_scale_matrix) {
    for (int qp = 0; qp <= 51; qp++) {
        for (int i = 0; i < 4; i++) {
            for (int j = 0; j < 4; j++) {
                ctx->levelScaleTable[qp][i][j] = norm_adjust_4x4[qp%6][(i&1) + (j&1)] * weight_scale_matrix[(i<<2) + j];
            }
        }
    }
}